package com.jain.shreyash.myapplication;

class ExpenseBoard {
    public float diet_break;
    public float diet_dinner;
    public float diet_lunch;
    public float eta_cost;
    public float extra;
    public float previous_cost;
    public float service_charge;
    public float total_cost;
    public float total_diets;

    public ExpenseBoard() {
    }

    public float getDiet_break() {
        return diet_break;
    }

    public void setDiet_break(float diet_break) {
        this.diet_break = diet_break;
    }

    public float getDiet_dinner() {
        return diet_dinner;
    }

    public void setDiet_dinner(float diet_dinner) {
        this.diet_dinner = diet_dinner;
    }

    public float getDiet_lunch() {
        return diet_lunch;
    }

    public void setDiet_lunch(float diet_lunch) {
        this.diet_lunch = diet_lunch;
    }

    public float getEta_cost() {
        return eta_cost;
    }

    public void setEta_cost(float eta_cost) {
        this.eta_cost = eta_cost;
    }

    public float getExtra() {
        return extra;
    }

    public void setExtra(float extra) {
        this.extra = extra;
    }

    public float getPrevious_cost() {
        return previous_cost;
    }

    public void setPrevious_cost(float previous_cost) {
        this.previous_cost = previous_cost;
    }

    public float getService_charge() {
        return service_charge;
    }

    public void setService_charge(float service_charge) {
        this.service_charge = service_charge;
    }

    public float getTotal_cost() {
        return total_cost;
    }

    public void setTotal_cost(float total_cost) {
        this.total_cost = total_cost;
    }

    public float getTotal_diets() {
        return total_diets;
    }

    public void setTotal_diets(float total_diets) {
        this.total_diets = total_diets;
    }
}
